package konversipaksa;

public class KonversiPaksa {

    public static void main(String[] args) {
        //KONVERSI PAKSA
        
        long a = 69;
        int b = (int)a;
        short c = (short)b;
        byte d = (byte)c;
        double e = (double)d;
        float f = (float)e;
        
        
        System.out.println("long ke int \t= "+b);
        System.out.println("int ke short \t= "+c);
        System.out.println("short ke byte \t= "+d);
        System.out.println("byte ke double \t= "+e);
        System.out.println("double ke float = "+f);
                   }
                
                }
