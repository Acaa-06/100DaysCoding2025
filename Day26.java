package day26;
public class Day26 {

    public static void main(String[] args) {
       
        int a = 10; 
        int b = 6;
        
        a += b;
        System.out.println(a);//16
        a -= b;
        System.out.println(a);//10
        a /= b;
        System.out.println(a);//1
        a *= b;
        System.out.println(a);//6
        a %= b;
        System.out.println(a);//0
        
        
    }
    
}
